#include <stdio.h>
#include <stdlib.h>

int	main(int argc, char **argv)
{
	int	i;
	int	number;

	if (argc == 2)
	{
		i = 1;
		number = atoi(argv[1]);
		if (number == 1)
			printf("1");
		while (number >= ++i)
		{
			if (number % i == 0)
			{
				printf("%d", i);
				number = number / i;
				if (number == 1)
					break ;
				printf("*");
				i = 1;
			}
		}
	}
	printf("\n");
	return (0);
}
