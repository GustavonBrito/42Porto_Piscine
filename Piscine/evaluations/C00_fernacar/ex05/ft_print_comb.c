#include <unistd.h>

void	ft_print_comb(void)
{
	char	n[3];

	n[0] = '0';
	n[1] = '0';
	n[2] = '0';
	while (n[0] != '9' || n[1] != '9' || n[2] != '9')
	{
		if (n[2] > n[1] && n[1] > n[0])
		{
			write(1, &n, 3);
			if (n[0] != '7' || n[1] != '8' || n[2] != '9')
				write(1, ", ", 2);
		}
		n[2]++;
		if (n[2] > '9')
		{
			n[2] = '0';
			n[1]++;
			if (n[1] > '9')
			{
				n[1] = '0';
				n[0]++;
			}	
		}
	}
}
