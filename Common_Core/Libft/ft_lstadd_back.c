#include "libft.h"

t_list	*ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*aux;

	if (*lst == NULL)
	{
		*lst = new;
		return (*lst);
	}
	aux = ft_lstlast(*lst);
	aux->next = new;
	return (*lst);
}
